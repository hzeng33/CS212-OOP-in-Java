public class Example3{
	public static void main(String args[]){
 		int num = 5;
   
   	  	//allows user to enter an integer
        	System.out.println("The number 5:");

        	//once entered, use the operators
   
   	  	//"post" increment:
   	  	System.out.print(num++);

   	  	//"pre" increment:
   	  	System.out.print(++num);

   	  	//"post" decrement:
   	  	System.out.print(num--);

   	  	//"pre" decrement:
   	  	System.out.print(--num);
	} //main
} //class


