public class Example1{
	public static void main(String args[]){
		int num = 5;
		
               //determine if number is even or odd
		if(num % 2 == 0){
		   System.out.println(num + " is even!");
		}else{
		   System.out.println(num + " is odd!");
		}

		//now try it again with another value
		num = 4;

               //determine if number is even or odd
		if(num % 2 == 0){
		   System.out.println(num + " is even!");
		}else{
		   System.out.println(num + " is odd!");
		}
 	} //main
} //class


